#include <string.h>

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	char *temp;

	temp = s1;
	if (*s2 == '\0')
		return (NULL);
	while (*s1 != '\0')
		s1++;
	while ((n > 0) && (*s1 = *s2))
	{
		s1++;
		s2++;
		n--;
	}
	*s1 = '\0';
	return (temp);
}
