#include <string.h>

char	*ft_strcat(char *s1, const char *s2)
{
	char	*temp;

	temp = s1;
	if (*s2 == '\0')
		return (NULL);
	while (*s1 != '\0')
		s1++;
	while (*s2 = *s1)
	{
		s1++;
		s2++;
	}
	return (temp);
}
