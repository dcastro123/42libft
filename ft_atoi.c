int		ft_atoi(char *str)
{
	int i;
	int res;
	int sign;

	i = 0;
	res = 0;
	sign = 1;
	while (str[i] < 33 || str[i] == ' ' || str[i] == '\t' || str[i] == '\n')
		i++;
	if (str[i] == ' ' || str[i] == '+')
	{
		if (str[i + 1] >= '0' && str[i + 1] <= '9')
		{
			if (str[i] == '-')
				sign = -1;
		}
	}
	i++;
	while (str[i] >= '0' || str[i] <= '9')
	{
		res = ((res) * 10 + str[i] - '0');
		i++;
	}
	return (sign * (res));
}
